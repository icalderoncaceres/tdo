<div class="col-xs-12"><center><h1><u>Bienvenida</u></h1></center></div>
<p class="t26 text-justify" id="bienvenida" name="bienvenida">
	El equipo de trabajo de <strong>SISTEMA CALDERON y EDUCACIONENLINEA.COM.VE, </strong> te da la Bienvenida a nuestro programa de aprendizaje del idioma m&aacute;s hablado del mundo, 
	la presenta secci&oacute;n es la &uacute;nica que encontraras en castellano, ya que en todas las dem&aacute;s
	tendras contacto con la lengua inglesa en diferentes formatos y actividades que te permitir&aacute;n ir adquiriendo las
	destrezas necesarias para comunicarte verbal y por escrito en la lengua anglosajona.
</p>
<p class="t26 text-justify">
	Ten muy presente de que no existe ninguna f&oacute;rmula m&aacute;gica para el aprendizaje, que es producto
	del esfuerzo y la constancia, as&iacute; que para que est&eacute; sistema te sea beneficioso, es necesario que le dediques
	el mayor tiempo posible adem&aacute;s de la mayor concentraci&oacute;n a la hora de realizar las actividades.
</p>
<br>
<center><h1><u>Estructura del  Programa</u></h1></center>
<p class="t26 text-justify" id="extructura" name="extructura">
	EL programa se divide en 3 niveles: b&aacute;sico (Basic), intermedio (Intermediate) y avanzado (Advance), cada nivel
	tiene 20 unidades y cada unidad 4 lecciones, para un total de 240 lecciones, cada una cuenta con: 
	escena de una entretenida historia, aproximadamente 100 audios, secci&oacute;n de gram&aacute;tica, 50 ejercicios, 30 palabras y frases que enriquecer&aacute;n 
	tu vocabulario, adem&aacute;s de esto, la videoteca donde podr&aacute;s acceder a tutoriales, canciones, cuentos y pel&iacute;culas
	ideales para el entrenamiento del o&iacute;do en la lengua inglesa, por otra parte, podr&aacute;s acceder a una serie de juegos entretenidos
	por medio de los cuales practicas mientras te diviertes, finalmente ponemos a tu disposici&oacute;n un laboratorio
	de idiomas para que vallas aprobando las diferentes lecciones demostrando los conocimientos adquiridos conversando con 
	nuestro profesor virtual y una prueba de nivel por medio de la cual puedes solicitar tu certificado. 
</p>
<br>
<center><h1><u>Como utilizar el  programa</u></h1></center>
<p class="t26 text-justify" id="instrucciones" name="instrucciones">
	No existe un orden para el uso de los recursos, depender&aacute; de c&oacute;mo te sientas m&aacute;s c&oacute;modo, sin embargo, 
	un orden recomendable puede ser:
	<div class="t20 text-justify instrucciones">
		<u>
			<li>Observa varias veces la escena correspondiente a la lecci&oacute;n actual, rep&iacute;tela hasta que reconozcas todas las 
			palabras y frases que all&iacute; aparecen no importa que no conozcas la traducci&oacute;n, despu&eacute;s de observar la escena 
			rep&iacute;tela pausando y repitiendo lo que dicen los personajes.
			</li><br>
			<li>Escucha los audios correspondiente al nivel en que te encuentres la mayor cantidad de veces posibles observando como
			se escribe en la parte central, de esta manera tu cerebro ira relacionando sonidos con texto.
			</li><br>
			<li>Estudia con detenimiento la lecci&oacute;n correspondiente, es bueno que estudies cada lecci&oacute;n varias veces hasta tener bien
			claro el contenido.
			</li><br>
			<li>Realiza varias veces los ejercicios correspondientes al nivel actual, cada secci&oacute;n de ejercicios tiene como
			m&iacute;nimo aprobatorio el 90% de aciertos, es importante realizar cada secci&oacute;n varias veces.
			</li><br>
			<li>Diariamente observa con detenimiento al menos un elemento de la videoteca, ya sea un tutorial, un cuento, una canci&oacute;n 
			o una pel&iacute;cula			
			</li><br>
			<li>Diariamente divi&eacute;rtete con los juegos que ponemos a tu disposici&oacute;n, de este modo practicaras en forma divertida.			
			</li><br>
			<li>Diariamente estudia al menos 20 palabras o frases de la secci&oacute;n de vocabulario, no necesitas memorizarlas basta con
			verlas, leerlas y relacionarlas con su significado en espa&ntilde;ol, tu cerebro poco a poco las ira reconociendo cuando las escuche.
			</li><br>
			<li>Cuando sientas que dominas el contenido de cada lecci&oacute;n utiliza el laboratorio, con el mismo podr&aacute;s demostrar tus avances
