			</li><br>
			<li>Cada vez que necesites traducir al espa&ntilde;ol puedes emplear nuestro traductor en l&iacute;nea el cual aparece en la parte derecha de 
			la pantalla, el mismo es un servicio privado que ofrece GOOGLE y que hemos contratado para ti.
			</li><br>
			<li>Ten en cuenta que entre mayor tiempo le dediques mas r&aacute;pido ser&aacute; el aprendizaje, tambi&eacute;n ten presente que para aprender
			un nuevo idioma el cerebro requiere entrenamiento, as&iacute; que si no entiendes en alg&uacute;n momento los audios o las escenas no te preocupes
			es normal, lo importante es escuchar y repetir con la mayor frecuencia posible.
			</li><br>
